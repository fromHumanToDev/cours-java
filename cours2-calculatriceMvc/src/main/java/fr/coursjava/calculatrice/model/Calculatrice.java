package fr.coursjava.calculatrice.model;

public class Calculatrice {

    public Integer add(Integer nombre1, Integer nombre2){
        return nombre1 + nombre2;
    }

    public Integer sub(Integer nombre1, Integer nombre2){
        return nombre1 - nombre2;
    }

    public Integer multiply(Integer nombre1, Integer nombre2){
        return nombre1 * nombre2;
    }

    public Integer divide(Integer nombre1, Integer nombre2){
        return nombre1 / nombre2;
    }
}
